$(document).ready(function() {
  $('#login_button').click(function(e){
    e.preventDefault();
    $.ajax({
      url:"/users/login",
      type:"GET"
    }).done(function(response){
      $('#login').html(response);
    });
  });
  $('#submit_login').submit(function(e){
    e.preventDefault();
    $.ajax({
      url:"/users/login",
      type:"POST",
      data:$('#login_form').serialize()
    }).done(function(response){
      $('#login').html("");
      $('#layout').html(response);
    });
  });
});
